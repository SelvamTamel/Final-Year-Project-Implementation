                        <p>In the current economic climate, it’s important to continually manage
                            cyber risks. Traditionally, people have been seen as the weakest link
                            in cyber security. Such thinking is now outdated. Many now agree that
                            people are our best defence. Equipping people with the skills they
                            need to protect themselves and their organisations is vital. Through a
                            positive psychology lens, we can move away from seeing people as
                            a “security problem” that needs fixing. We cannot “fix” human error or
                            change human irrationality. So how could we address people’s poor
                            security behaviours? Many are moving towards empowering people,
                            and harnessing their strengths and differences. Their aim is to build a
                            positive and personalised experience for all.
                        
                            One underlooked area is the role of personality traits. These are
                            the aspects that determine how people think, feel and behave.
                            What makes us different from one another? What role does
                            personality play in cyber security? Do personality traits make us
                            more susceptible to cyber crime? And most importantly, can we use
                            personality to improve cyber hygiene?
                        </p>
                        <blockquote class="blockquote">
                            <p class="mb-0">Much of the research on personality types and susceptibility to cyber
                                attacks is mixed. This points to the challenges of using personality
                                traits in cyber security training in an actionable way. A possible
                                reason for this may be insufficient interest in experimentation with a
                                model where the traits exist in a continuum (e.g. Big Five). Also, fairly
                                flexible sub-traits and self-reported personality surveys fuel research
                                disagreements.</p>
                        </blockquote>
                        <p>Personality traits are seen as the novel concept and somewhat
                            unexplored territory in practice. The research is often conducted with
                            student samples and not easily generalisable. It would be interesting
                            to incorporate such elements into workplace training programmes.
                            This could be done by adding a brief personality inventory (e.g. 44-
                            item BFI26) to tailor the types of security threats presented to a learner.
                            Additionally, we could tailor the training modules to suit the learner’s
                            personality trait. Here, the learner receives content that appeals to their
                            individual differences in personality. For example, extroverts would
                            receive content that appeals to their social preferences</p>

                        <p>Personality research in cyber security helps explain why different
                            people make different security decisions. Using this information,
                            we can tailor programmes to specific audiences to maximise
                            effectiveness. This is much like making a made-to-measure suit.
                            Tailor-made suits fit perfectly. Generic suits do not – no matter how
                            fine the underlying fabric might be</p>
                        

