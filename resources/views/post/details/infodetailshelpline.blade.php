                        <p>If someone is feeling depressed or suicidal, our first response is to try to help. We offer advice, share our own experiences, try to find solutions.
                            We'd do better to be quiet and listen. People who feel suicidal don't want answers or solutions. 
                            They want a safe place to express their fears and anxieties, to be themselves.
                           Listening - really listening - is not easy. We must control the urge to say something - to make a comment, add to a story or offer advice. 
                           We need to listen not just to the facts that the person is telling us but to the feelings that lie behind them. 
                           We need to understand things from their perspective, not ours.</p>
                        <p>Someone to listen. Someone who will take time to really listen to them. Someone who won't judge, or give advice or opinions, but will give their undivided attention.
                            Someone to trust. Someone who will respect them and won't try to take charge. Someone who will treat everything in complete confidence.
                            Someone to care. Someone who will make themselves available, put the person at ease and speak calmly. 
                            Someone who will reassure, accept and believe. Someone who will say, "I care."</p>      

