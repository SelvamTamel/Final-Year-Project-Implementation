                        <p>It is important for people to have the opportunity to explore difficult feelings. 
                            Being listened to in confidence, and accepted without prejudice, can alleviate general distress, despair and suicidal feelings.
                            Often being listened to is enough to help someone through a time of distress.  
                            Even just showing that you are there for them, and that you know they are going through a distressing time, can in itself be a comfort.
                        
                            Are you a good listener?

                            Do you:

                        1) Always try to give people your undivided attention?

                        2) Let them sit in silence and collect their thoughts if they need to?

                        3) Question them gently, tactfully and without intruding?

                        4) Encourage them to tell their story in their own words and in their own time?

                        5) Refrain from offering advice based on your own experience?

                        6) Always try and see their point of view even though you may not agree with it?

                        7) these can help show someone that you are really listening to them.

                        or Do you:

                        8) Look around the room or glance at your watch while they are talking?

                        9) Finish their sentences for them and correct their grammar?

                        10) Interrupt to tell them how you once had a similar problem?

                        11) Make a snap judgement based on their accent, dress or personal appearance?

                        12) Tell them what you would do in their position?

                        13) Say you understand before you've heard what their problem is?

                        </p>
